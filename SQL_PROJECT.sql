-- Q2: Determine the most popular music genre for each country based on purchases.
WITH GENRE_PURCHASES AS(
SELECT CUSTOMER.COUNTRY,GENRE.NAME AS GENRE_NAME,
COUNT(INVOICE_LINE.QUANTITY) AS TOTAL_PURCHASES 
FROM INVOICE_LINE
JOIN INVOICE
ON INVOICE.INVOICE_ID=INVOICE_LINE.INVOICE_ID
JOIN CUSTOMER
ON INVOICE.CUSTOMER_ID=CUSTOMER.CUSTOMER_ID
JOIN TRACK
ON INVOICE_LINE.TRACK_ID=TRACK.TRACK_ID
JOIN GENRE
ON TRACK.GENRE_ID=GENRE.GENRE_ID
GROUP BY
CUSTOMER.COUNTRY,GENRE_NAME
),
RANKED_GENRES AS(
SELECT 
COUNTRY,
GENRE_NAME,
TOTAL_PURCHASES,
RANK() OVER (
PARTITION BY COUNTRY 
ORDER BY TOTAL_PURCHASES DESC)
AS RANK_GENRE 
FROM GENRE_PURCHASES)
SELECT COUNTRY,GENRE_NAME,TOTAL_PURCHASES
FROM RANKED_GENRES
WHERE RANK_GENRE=1
ORDER BY COUNTRY;

-- Q3 Identify the top-spending customer for each country.
-- 1ST STEP CALCULATE TOTAL SPENDING OF EACH CUSTOMER
WITH CUSTOMERS_SPENDING AS (
SELECT
CUSTOMER.CUSTOMER_ID,
CUSTOMER.FIRST_NAME,
CUSTOMER.LAST_NAME,
CUSTOMER.COUNTRY,
SUM(INVOICE.TOTAL) AS TOTAL_SPENT
FROM  CUSTOMER
JOIN INVOICE
ON INVOICE.CUSTOMER_ID=CUSTOMER.CUSTOMER_ID
GROUP BY
CUSTOMER.CUSTOMER_ID,
CUSTOMER.FIRST_NAME,
CUSTOMER.LAST_NAME,
CUSTOMER.COUNTRY
)
-- 2ND PART 
,RANKED_CUSTOMER AS (
SELECT
*,
RANK() OVER(
PARTITION BY COUNTRY
ORDER BY TOTAL_SPENT DESC
) AS SPENDING_RANK
FROM CUSTOMERS_SPENDING
)
SELECT 
COUNTRY,
FIRST_NAME,
LAST_NAME,
TOTAL_SPENT
FROM RANKED_CUSTOMER
WHERE SPENDING_RANK=1
ORDER BY COUNTRY;



