FROM ARTIST
JOIN ALBUM2
ON ARTIST.ARTIST_ID=ALBUM2.ARTIST_ID
JOIN TRACK
ON ALBUM2.ALBUM_ID=TRACK.ALBUM_ID
JOIN GENRE
ON TRACK.GENRE_ID=GENRE.GENRE_ID
WHERE GENRE.NAME="ROCK"
group by ARTIST.ARTIST_ID,ARTIST.NAME
ORDER BY TRACK_COUNT DESC
LIMIT 10;

