-- Q1. CALCULATE HOW MUCH EACH CUSTOMER HAS SPENT ON EACH ARTIST
-- use of CTE
WITH ARTIST_SALES AS(
SELECT INVOICE.CUSTOMER_ID,
ARTIST.ARTIST_ID,ARTIST.NAME,
SUM(INVOICE_LINE.UNIT_PRICE*INVOICE_LINE.QUANTITY) AS TOTAL_SPENT
FROM INVOICE_LINE 
JOIN INVOICE
ON INVOICE.INVOICE_ID=INVOICE_LINE.INVOICE_ID
JOIN TRACK
ON INVOICE_LINE.TRACK_ID=TRACK.TRACK_ID
JOIN ALBUM2
ON TRACK.ALBUM_ID=ALBUM2.ALBUM_ID
JOIN ARTIST
ON ALBUM2.ARTIST_ID=ARTIST.ARTIST_ID
GROUP BY
INVOICE.CUSTOMER_ID,
ARTIST.ARTIST_ID,
ARTIST.NAME
)
SELECT 
CUSTOMER.CUSTOMER_ID,
CUSTOMER.FIRST_NAME,
CUSTOMER.LAST_NAME,
ARTIST_SALES.NAME,
ARTIST_SALES.TOTAL_SPENT
FROM ARTIST_SALES
JOIN CUSTOMER
ON ARTIST_SALES.CUSTOMER_ID=CUSTOMER.CUSTOMER_ID
ORDER BY
CUSTOMER.CUSTOMER_ID,
ARTIST_SALES.TOTAL_SPENT DESC;
