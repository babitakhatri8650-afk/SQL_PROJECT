ON INVOICE.INVOICE_ID=INVOICE_LINE.INVOICE_ID
JOIN TRACK
ON INVOICE_LINE.TRACK_ID=TRACK.TRACK_ID
JOIN GENRE
ON TRACK.GENRE_ID=GENRE.GENRE_ID
WHERE GENRE.NAME="ROCK";

-- Q2 TOP 10 ROCK ARTISTS BASED ON TRACK COUNT
